/**
 * Get random images for a property based on city and type
 */
export function getPropertyImages(city: string, type: string, count: number = 6): string[] {
  const cityKey = city.toLowerCase().replace('ş', 's').replace('ç', 'c').replace('ı', 'i');
  const typeKey = type === 'villa' ? 'villas' : 'apartments';

  const cityImages = propertyImages[cityKey as keyof typeof propertyImages];
  if (!cityImages || typeof cityImages === 'string') return [];

  const typeImages = (cityImages as any)[typeKey] || [];
  const allImages = [
    ...typeImages,
    ...propertyImages.interiors.livingRooms,
    ...propertyImages.interiors.bedrooms,
    ...propertyImages.outdoor.pools,
    ...propertyImages.views.seaViews,
  ];

  // Get random unique images
  const shuffled = allImages.sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}

/**
 * Get images for specific property categories
 */
export function getCategoryImages(category: 'luxury' | 'beachfront' | 'modern' | 'traditional'): string[] {
  switch (category) {
    case 'luxury':
      return [
        ...propertyImages.antalya.villas.slice(0, 3),
        ...propertyImages.interiors.livingRooms,
        ...propertyImages.outdoor.pools,
      ];
    case 'beachfront':
      return [
        ...propertyImages.marmaris.villas,
        ...propertyImages.views.seaViews,
      ];
    case 'modern':
      return [
        ...propertyImages.cesme.villas,
        ...propertyImages.interiors.livingRooms,
      ];
    case 'traditional':
      return [
        ...propertyImages.bodrum.villas,
        ...propertyImages.outdoor.gardens,
      ];
    default:
      return [];
  }
}
